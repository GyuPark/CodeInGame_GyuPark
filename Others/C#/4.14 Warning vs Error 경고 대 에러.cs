문제를 고립시키고자 한다면 한 행에 각 구문을 유지해서 중단점(break point)을 각 행에 적용하는 것이 중요하다.
중단점은 코드의 특정 지점에서 프로그램의 실행을 멈춘다.
코드를 간결하고 읽기 쉽게 유지하는 것이 중요하지만 디버거가 작업을 할 수 있도록 만드는 것도 중요하다.
*/