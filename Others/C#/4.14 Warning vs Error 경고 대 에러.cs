/*
앞에서 설명했듯이 compile time error는 클래스가 컴파일되는 것을 모두 중단하게 만든다.
이런 에러 대부분은 문법 또는 타입 캐스팅 에러이고 코드의 나머지 부분이 동작하는 것을 막게 된다.

한편, 게임이 실행 중일 때 나타나는 에러는 runtime error라고 한다.
추적하기가 어려워 debugging session이 필요하다. 

경고는 무해한 경우가 많다.
가장 일반적인 경고는 사용하지 않는 변수 선언에 당신이 관심을 가지도록 하기 위한 것이다.
*/


