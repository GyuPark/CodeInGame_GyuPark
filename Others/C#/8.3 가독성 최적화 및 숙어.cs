int GreaterThan(int a, int b)
{
	return (a>b) ? a : b; //a가 b보다 더 큰 경우라면 a를 반환하고 그렇지 않은 경우 b를 반환한다.
}

int GreaterThan(int a, int b, int c)
{
	return (a>b) ? ((a>c) ? a: c) : (b>c) ? b : c); 세 개의 값을 모두 비교하고 가장 큰 값을 반환한다.
}



