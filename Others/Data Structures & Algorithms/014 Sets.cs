Sets

A set is a collection of unique elements.
The elements are called members. 
The two most important properties of sets are that members of a set are unordered and 
no member can occur in a set more than once. Sets play a very important role in CS but are not included
as a data structure in C#.

------------------------
Sets and set theory provide much of the foundation of computer science theory.
C# does not provide a set data type or data structure.